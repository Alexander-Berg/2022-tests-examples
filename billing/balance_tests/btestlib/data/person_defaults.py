class InnType(Enum):
    """
    RANDOM - Генерируется случайный валидный ИНН
             для тестов где для плательщика будет вызываться выгрузка в ОЕБС, но его уникальность в рамках ОЕБС необязательна
             (нам неважно каким методом будет происходить выгрузка create или update)
    UNIQUE - Генерируется валидный ИНН, которого еще нет в ОЕБС
             для тестов где для плательщика будет вызываться выгрузка в ОЕБС и необходима его уникальность в рамках ОЕБС
             (нам нужна выгрузка через метод create)
    DEFAULT - Используется ИНН из person_defaults
    """
    DEFAULT = 0
    RANDOM = 1
    UNIQUE = 2


