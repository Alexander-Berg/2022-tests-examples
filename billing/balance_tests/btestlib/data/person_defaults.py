def get_pfr_number(pfr, pfr_type=None):
    # если ничего не задано, то генерим случайный
    if pfr_type is None:
        pfr_type = PfrType.RANDOM

    if pfr_type == PfrType.RANDOM:
        pfr_number = str(random.randint(100000000, 999999999))
        control_num = sum([int(pfr_number[-i]) * i for i in range(1, 10)])
        if control_num > 100:
            control_num %= 101
        if control_num == 100:
            control_num = 0
        return '-'.join([pfr_number[0:3], pfr_number[3:6], pfr_number[6:9]]) + ' ' + str(control_num).zfill(2)
    else:
        return pfr


def get_account(type_, account, account_type=None, bik=None):
    # если ничего не задано, то генерим случайный
    if account_type is None:
        account_type = AccountType.RANDOM

    if account_type == AccountType.RANDOM and type_ in ['ur', 'ph'] and bik is not None:
        acc_draft = str(bik)[-3:] + '40702810' + '0' + str(random.randint(10000000000, 99999999999))
        org_key = sum([x * y for (x, y) in zip([int(x) for x in acc_draft], ([7, 1, 3] * 8)[:23])]) * 3 % 10
        return acc_draft[3:11] + str(org_key) + acc_draft[12:]
    elif account_type == AccountType.RANDOM:
        return ''.join([str(random.randint(0, 9)) if symbol.isdigit() else symbol for symbol in account])
    else:
        return account


