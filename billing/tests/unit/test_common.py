def test_dict_update():
    """ Мы опираемся на этот синтаксис при создании артефактов с метаданными. """
    d1 = {1: 1, 2: 1}
    d2 = {1: 2, 3: 2}
    assert {**d1, **d2} == {1: 2, 2: 1, 3: 2}
    assert {**d2, **d1} == {1: 1, 2: 1, 3: 2}
    assert {**d1, 1: 2} == {1: 2, 2: 1}
    assert {1: 2, **d1} == d1
